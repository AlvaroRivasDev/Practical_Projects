// Los metodos son como funciones especiales de un elemento

// Un texto tiene metodos, los cuales se pueden usar de la siguiente manera

let texto = "Soy un texto de ejemplo ";

console.log(texto.length);
console.log(texto.toUpperCase());
console.log(texto.toLowerCase());
console.log(texto.split());
console.log(texto.match());
console.log(texto.repeat(2));
console.log(texto.replace());
console.log(texto.endsWith(texto));

// Los metodos nos proporcionan informacion de un elemento de distintas maneras, cargadas desde funciones o contexto puro